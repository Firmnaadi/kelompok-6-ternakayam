package com.example.ayamku;

public class Ayam {
    private String idAyam;
    private String jumlahAyam;

    public Ayam(String idAyam, String jumlahAyam) {
        this.idAyam = idAyam;
        this.jumlahAyam = jumlahAyam;
    }

    public String getIdAyam() {
        return idAyam;
    }

    public String getJumlahAyam() {
        return jumlahAyam;
    }
}
